package com.callor.var;

public class VarA {
	public static void main(String[] args) {

		int num1 = 0;
		int num2 = 0;

		num1 = 1;
		num2 = 2;
		
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);

		System.out.println(num1 / num2);
		System.out.println(num1 % num2);

	}
}
