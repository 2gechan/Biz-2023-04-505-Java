package com.callor.system;

// class 이름은 반드시 첫글자가 대문자
// 두단어 이상 조합하는 것을 권장
// Camel case로 작성
public class SystemA {
	
	public static void main(String[] args) {
		System.out.println("Hello");
		System.err.println("반갑습니다");
		System.out.println();
		
		System.out.print(30);
		System.out.print("+");
		System.out.print(40);
		System.out.print("=");
		System.out.print(30 + 40 + "\n");
		System.out.println("안녕하세요");
	}

}
